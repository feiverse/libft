#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	v;

	v = 0;
	if (!s)
		return ;
	while (s[v])
	{
		(*f)(v, (s + v));
		++v;
	}
}
