#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	v;
	size_t	len;
	char	*new;

	v = 0;
	if (!s)
		return (ft_strdup(""));
	len = ft_strlen(s);
	new = (char *)malloc(sizeof(char) * (len + 1));
	if (!new)
		return (NULL);
	while (v < len)
	{
		new[v] = (*f)(v, s[v]);
		++v;
	}
	new[v] = 0;
	return (new);
}
